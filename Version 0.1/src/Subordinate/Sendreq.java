    public void setreq() throws FileNotFoundException, IOException, SQLException, ClassNotFoundException {
        FileReader fr = new FileReader("request.txt");
        BufferedReader br = new BufferedReader(fr);
        int idrequest = parseInt(br.readLine());
        Connection connect = new Connection();
        Statement stmt = connect.con.createStatement();
        String SQL = "";
        if (jRadioButton2.isSelected()) {
            SQL = "Update request set finished =" + 1 + ",error =" + 2 + " where IDREQUEST=" + idrequest;
        }
        if (jRadioButton3.isSelected()) {
            SQL = "Update request set finished =" + 2 + ",error =" + 1 + " where IDREQUEST=" + idrequest;
        }
        stmt.executeUpdate(SQL);
    }
    public void sendReport() throws FileNotFoundException, IOException, SQLException, ClassNotFoundException{
        boolean Exist = false;
        FileReader fr = new FileReader("request.txt");
        BufferedReader br = new BufferedReader(fr);
        int idrequest = parseInt(br.readLine());
        Connection connect = new Connection();
        Statement stmt = connect.con.createStatement();
        String SQL = "Select idrequest from report where idrequest ="+ idrequest;
        ResultSet results = stmt.executeQuery(SQL);
        if(results.next()) Exist = true;
        else Exist = false;
        SQL = "Select idrequest from report";
        results = stmt.executeQuery(SQL);
        int idreport = 1 ;
        while(results.next()) idreport++;
        Calendar cal = Calendar.getInstance();
        if(!Exist) SQL = "INSERT INTO report(IDREPORT,IDREQUEST,REPORT,REPORTDATE)"+"values ('"+idreport+"','"+idrequest+"','"+ jTextArea1.getText() +"','"+ new SimpleDateFormat("yyyy-MM-dd HH:mm:ss").format(cal.getTime()) + "')";
        else SQL = "update report set report = '" + jTextArea1.getText()+ "' WHERE IDREQUEST = " +idrequest ;
        stmt.executeUpdate(SQL);
    }
    public void setWindows() throws FileNotFoundException, IOException, SQLException, ClassNotFoundException {
        jLabel3.setVisible(false);
        FileReader fr = new FileReader("request.txt");
        BufferedReader br = new BufferedReader(fr);
         int idrequest = parseInt(br.readLine());
        Connection connect = new Connection();
        Statement stmt = connect.con.createStatement();
        String SQL = "SELECT * from request where idrequest =" + idrequest;
        ResultSet results = stmt.executeQuery(SQL);
        results.next();
        if (results.getInt("ERROR") == 2) {
            jRadioButton2.setSelected(true);
        }
        if (results.getInt("FINISHED") == 2) {
            jRadioButton3.setSelected(true);
        }
        SQL = "SELECT * from REPORT where idrequest =" + idrequest;
        results = stmt.executeQuery(SQL);
        if(results.next()) jTextArea1.setText(results.getString("REPORT"));
        
    }
